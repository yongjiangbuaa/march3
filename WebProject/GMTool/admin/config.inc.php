function getGPC($name,$type){
	if ($type == 1 && in_array($name, array('username','useruid'))) {
		switch ($name) {
			case 'username':
				setcookie('u2',$_REQUEST[$name],time()+3600);
				break;
			case 'useruid':
				setcookie('u3',$_REQUEST[$name],time()+3600);
				break;
			default:
				break;
		}
	}
	return $_REQUEST[$name];
}

