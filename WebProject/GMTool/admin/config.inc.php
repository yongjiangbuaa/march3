		foreach ($tmp as $tt) {
			$tt = trim($tt);
			if (!empty($tt)) {
				if(strstr($tt,'-')){
					$ttArray=explode('-', $tt);
					$min=min($ttArray[1],$maxServer);
					for ($i=$ttArray[0];$i<=$min;$i++){
						$selectServer['s'.$i] = '';
						$selectId[]=intval($i);
					}
