	$arr = array('kill_titan','exchange','quest','quest_mark','mark','general','events','item','alliance_phase','reward','equipment');

	if(in_array($xmlName,$arr)){
		//后端配置表
		$path = "/data/htdocs/resource/$xmlName.xml";
		if(!file_exists($path)){
			$path = ITEM_ROOT."/$xmlName.xml";
		}
	}else{
		//前端配置表
		$path = "/data/htdocs/ifadmin/admin/xml/front_xml/$xmlName.xml";
		if(!file_exists($path)) {
			$path = ITEM_ROOT . "/$xmlName.xml";
		}
	}
	$items = simplexml_load_file($path);
	if($groupName){
		$nodes = $items->xpath('//Group[@id=\''.$groupName.'\']');
		$items = $nodes[0];
	}
	foreach ($items as $item)
	{
		$itemDetail[(int)$item['id']] = $item;
	}
	return $itemDetail;
}
function loadLanguage($lang = 'zh_CN'){
	$path = "/data/htdocs/ifadmin/admin/xml/front_xml/text_$lang.ini";

	if(file_exists($path)){
		$langItems = file($path);
	}else {
		$langItems = file(ITEM_ROOT . '/text_' . $lang . '.ini');
	}

	$lang = array();
	foreach ($langItems as $langItem) {
		$index = strpos($langItem,"=");
		if($index != false)
			$lang[substr($langItem, 0, $index)] = substr($langItem,$index+1);
	}
	return $lang;
}
function strreplace(/* polymorphic */)
{
	$args = func_get_args();
	$argNums = func_num_args() - 1;
	$content = $args[0];
	for($key=0;$key<$argNums;)
		$content = str_replace("{" . $key . "}", $args[++$key], $content);
	return $content;
}
