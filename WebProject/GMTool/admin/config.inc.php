function getVipLevel($score){
	$vipxml = loadXml('database.local','vip');
	$level = 0;
	foreach ($vipxml as $value){
		if(intval($value['point']) <= $score){
			$level = $value['id'];
		}
	}
	return ($level%100);
}
function readIniFile($configfilename) {
	$configs = array ();
	$rows = @file ( $configfilename );
	foreach ( $rows as $row ) {
		$config = trim ( $row );
		if (empty ( $config )) {
			continue;
		}
		if ($config [0] != "#") {
			$pos = strpos ( $config, "#", 0 );
			if ($pos) {
				$config = substr ( $config, 0, $pos );
			}
			if ($pos = strpos ( $config, "=", 0 )) {
				$key = trim ( substr ( $config, 0, $pos ) );
				$value = trim ( substr ( $config, $pos + 1 ) );
				$configs ["$key"] = $value;
			}
		}
	}
	return $configs;
}
